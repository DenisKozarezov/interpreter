package tests

import (
	"strings"
	"testing"

	"interpreter/internal/lexer"
	"interpreter/internal/parser"

	"github.com/stretchr/testify/require"
)

func TestOperatorPrecedence(t *testing.T) {
	for _, tt := range []struct {
		source   string
		expected string
	}{
		{
			"-a * b",
			"((-a) * b)",
		},
		{
			"!-a",
			"(!(-a))",
		},
		{
			"a + b + c",
			"((a + b) + c)",
		},
		{
			"a + b - c",
			"((a + b) - c)",
		},
		{
			"a * b * c",
			"((a * b) * c)",
		},
		{
			"a * b / c",
			"((a * b) / c)",
		},
		{
			"a + b / c",
			"(a + (b / c))",
		},
		{
			"a + b * c + d / e - f",
			"(((a + (b * c)) + (d / e)) - f)",
		},
		{
			"a + b + c + d * e",
			"(((a + b) + c) + (d * e))",
		},
		{
			"3 + 4; -5 * 5",
			"(3 + 4)((-5) * 5)",
		},
		{
			"5 > 4 == 3 < 4",
			"((5 > 4) == (3 < 4))",
		},
		{
			"5 < 4 != 3 > 4",
			"((5 < 4) != (3 > 4))",
		},
		{
			"3 + 4 * 5 == 3 * 1 + 4 * 5",
			"((3 + (4 * 5)) == ((3 * 1) + (4 * 5)))",
		},
		{
			"3 + 4 * 5 == 3 * 1 + 4 * 5",
			"((3 + (4 * 5)) == ((3 * 1) + (4 * 5)))",
		},
		{
			"true",
			"true",
		},
		{
			"false",
			"false",
		},
		{
			"3 > 5 == false",
			"((3 > 5) == false)",
		},
		{
			"3 < 5 == true",
			"((3 < 5) == true)",
		},
	} {
		t.Run(tt.source, func(t *testing.T) {
			// 1. Arrange
			l := lexer.NewLexer(strings.NewReader(tt.source))
			p := parser.NewParser(l)

			// 2. Act
			program := p.Parse()
			got := program.String()

			// 3. Assert
			require.Len(t, p.Errors(), 0)
			require.Equal(t, tt.expected, got)
		})
	}
}
